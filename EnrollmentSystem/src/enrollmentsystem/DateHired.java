package enrollmentsystem;

/**
 *
 * @author Betelgeuse
 */
class DateHired {

    private int date;
    private String month;
    private int year;

    public DateHired(int date, String month, int year) {
        this.date = date;
        this.month = month;
        this.year = year;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getDetails() {
        return "Date hired: " + month + " " + date + ", " + year;
    }
}
